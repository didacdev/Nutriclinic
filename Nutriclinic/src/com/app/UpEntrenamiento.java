    /**
     * Actualiza el bono del cliente seleccionado
     * @param evt 
     */
    private void buttonMousePressed(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_buttonMousePressed
        if(bono.getText().equals("Ingrese el bono") || fecha.getText().equals("Ingrese la fecha de inicio")){
            javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
            bono.requestFocus();
        }
        else
        {
            String numberBono = bono.getText();
            String fechaInicio = fecha.getText();

            if(numberBono == null || "".equals(numberBono) || fechaInicio == null || "".equals(fechaInicio)){
                javax.swing.JOptionPane.showMessageDialog(this, "Debe llenar todos los campos \n", "AVISO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
                bono.requestFocus();
            }
            else{ 
                 edit(Integer.parseInt(origId), Integer.parseInt(numberBono), fechaInicio);
                    
                bono.setText("");
                fecha.setText("");

                Entrenamiento p1 = new Entrenamiento();
                p1.setSize(750, 430);
                p1.setLocation(0, 0);

                content.removeAll();
                content.add(p1, BorderLayout.CENTER);
                content.revalidate();
                content.repaint();
                
            }
        }
    }//GEN-LAST:event_buttonMousePressed

