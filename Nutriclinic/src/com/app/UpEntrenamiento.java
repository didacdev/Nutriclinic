    /**
     * Edita el valor del atributo bono del cliente con el id introducido como par√°metro en la base de datos
     * @param id
     * @param bono
     * @throws SQLException 
     */
    public void edit(int id, int bono, String fechaInicio){
        try{
            statement.updateBono(id, bono, fechaInicio);
            javax.swing.JOptionPane.showMessageDialog(this, "Datos editados correctamente \n", "HECHO", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        catch(SQLException e){
            javax.swing.JOptionPane.showMessageDialog(this, "No se han podido editar los datos \n", "ERROR", javax.swing.JOptionPane.INFORMATION_MESSAGE);
        }
        
        
    }

